#include "libft.h"

void	ft_putstr_fpf(char *str, int c)
{
	int i;

	i = 0;
	while (1)
	{
		if (str[i] == '\0' && !c--)
			break ;
		ft_putchar_fd(str[i++], 1);
	}
}
